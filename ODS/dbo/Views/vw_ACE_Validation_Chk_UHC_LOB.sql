CREATE VIEW [dbo].[vw_ACE_Validation_Chk_UHC_LOB]
AS  -- If this returns: LOB1	PLANNAME no issues were found.
    -- This should result only Issues needed attention: Bad LOB maps, Questionalbe LOB Maps
    -- It selects all subgroup names, and maps them to the AHS export LOB name (mapping table)
    -- We need to revise the language of the purpose of this validtion
     SELECT DISTINCT 
            'LOB1' AS 'UHC LOB', 
            'PLANNAME' AS 'UHC PLAN NAME'
     FROM
     (
         SELECT DISTINCT
                (CASE
                     WHEN(LINE_OF_bUSINESS IS NULL)
                     THEN 'NULLVALUE'
                     ELSE LINE_OF_BUSINESS
                 END) AS 'LOB1', 
                SUBGRP_NAME AS 'PLANNAME'
         FROM dbo.vw_UHC_ActiveMembers
     ) AS TAB
     WHERE(RTRIM('LOB1') NOT IN
     (
         SELECT SOURCE_VALUE
         FROM dbo.ALT_MAPPING_TABLES
         WHERE(TYPE = 'LINE OF BUSINESS')
              AND (DESTINATION = 'ALTRUISTA')
              AND (SOURCE = 'UHC')
     ));

GO
